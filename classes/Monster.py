
class Monster:

    def __init__(self, armorClass, hp, speed, str, dex, const, intl, wis, cha, skills, senses, languages, challenge):
        self.armorClass = armorClass
        self.hp = hp
        self.speed = speed
        self.str = str
        self.dex = dex
        self.const = const
        self.intl = intl
        self.wis = wis
        self.cha = cha
        self.skills = skills
        self.senses = senses
        self.languages = languages
        self.challenge = challenge

