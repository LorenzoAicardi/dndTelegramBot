class Equipment:
    def __init__(self, armor, spellCast, weapons, advGear, tools, mounts, wealth, trinkets):
        self.armor = armor,
        self.spellCast = spellCast
        self.weapons = weapons,
        self.advGear = advGear,
        self.tools = tools,
        self.mounts = mounts,
        self.wealth = wealth,
        self.trinkets = trinkets